     void populateRecord(){
        
        
       outPatientName.setText(this.medRepo.getPatient());   
        DefaultTableModel model = (DefaultTableModel) tblMedReport.getModel();
        
        testParameters = this.medRepo.getTestParameters();
        
        Object[] row = new Object[5];
        model.setRowCount(0);
        for (Map.Entry mapElement : testParameters.entrySet()) { 
            String key = (String)mapElement.getKey(); 
            String value = (String) mapElement.getValue();
  
             
    

                       // Object[] row = new Object[5];
                    
                        row[0] = key;
                        row[1] = value;
                        model.addRow(row);
                    
          }

    };

