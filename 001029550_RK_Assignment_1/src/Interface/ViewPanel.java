    private void displayTexts(PersonalInfo person)
    {
        
        // Setting text fields values from the demo object in person object
        firstNameTextField1.setText(person.getDemoInfo().getFirstName());
        lastNameTextField.setText(person.getDemoInfo().getLastName());
        ssnTextField.setText(person.getDemoInfo().getSSN());
        ageTextField.setText(person.getDemoInfo().getAge());
        heightTextField.setText(person.getDemoInfo().getHeight());
        phoneNumTextField.setText(person.getDemoInfo().getPhoneNum());
        dobTextField.setText(person.getDemoInfo().getDob());
        weightTextField.setText(person.getDemoInfo().getWeight());
        
        
        // Address info
        streetAddressTextField.setText(person.getAddInfo().getAddress());
        cityTextField.setText(person.getAddInfo().getCity());       
        stateTextField.setText(person.getAddInfo().getState());        
        zipCodeTextField.setText(person.getAddInfo().getZip());        
        
        // Savings info
        bankAccTextField1.setText(person.getSavingsAccInfo().getBankAccNum());
        balanceTextField1.setText(person.getSavingsAccInfo().getAccBalance());
        accTypeTextField1.setText(person.getSavingsAccInfo().getAccType());
        bankNameTextField1.setText(person.getSavingsAccInfo().getBankName());        
        routingNumTextField1.setText(person.getSavingsAccInfo().getBankRoutingNum());
        
        
        // Closing info
        bankAccTextField2.setText(person.getCheckAccInfo().getBankAccNum());
        balanceTextField2.setText(person.getCheckAccInfo().getAccBalance());
        accTypeTextField2.setText(person.getCheckAccInfo().getAccType());
        bankNameTextField2.setText(person.getCheckAccInfo().getBankName());        
        routingNumTextField2.setText(person.getCheckAccInfo().getBankRoutingNum());
                
        
        // Driver's info
        licenseNumTextField.setText(person.getDriversLicInfo().getLicNum());
        bloodTypeTextField.setText(person.getDriversLicInfo().getBloodType());        
        issueDateTextField.setText(person.getDriversLicInfo().getIssueDate());       
        expTextField.setText(person.getDriversLicInfo().getExpDate());
        

        // Medical info
        
        medRecNumTextField.setText(person.getMedInfo().getMedicalNum());        
        alg1TextField.setText(person.getMedInfo().getAlg1());
        alg2TextField.setText(person.getMedInfo().getAlg2());        
        alg3TextField.setText(person.getMedInfo().getAlg3());        
        
        
        // Image Label
        ImageLabel.setIcon(person.getDriversLicInfo().getImage());
    
    }
    
